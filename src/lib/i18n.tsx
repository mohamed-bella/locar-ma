function interpolate(str: string, vars?: Record<string, string | number>) {
  if (!vars) return str
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`))
}

type Ctx = {
  locale: Locale
  setLocale: (l: Locale) => void
  t: (key: string, vars?: Record<string, string | number>) => string
}

const I18nContext = createContext<Ctx | null>(null)

const ONE_YEAR = 60 * 60 * 24 * 365

export function I18nProvider({
  initialLocale,
  children,
}: {
  initialLocale: Locale
  children: React.ReactNode
}) {
  const [locale, setLocaleState] = useState<Locale>(initialLocale)

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l)
    if (typeof document !== 'undefined') {
      document.cookie = `locale=${l}; path=/; max-age=${ONE_YEAR}; samesite=lax`
      document.documentElement.lang = l
      document.documentElement.dir = isRtl(l) ? 'rtl' : 'ltr'
    }
  }, [])

  useEffect(() => {
    if (typeof document !== 'undefined') {
      document.documentElement.lang = locale
      document.documentElement.dir = isRtl(locale) ? 'rtl' : 'ltr'
    }
  }, [locale])

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      // Darija is a flat overlay; anything not translated yet falls back to
      // French (closer for a Moroccan user than English), then the key.
      if (locale === 'ar') return interpolate(AR[key] ?? M[key]?.fr ?? M[key]?.en ?? key, vars)
      const entry = M[key]
      if (!entry) return key
      return interpolate(entry[locale] ?? entry.en, vars)
    },
    [locale],
  )

  const value = useMemo<Ctx>(() => ({ locale, setLocale, t }), [locale, setLocale, t])
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): Ctx {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}

// Convenience: just the translator.
export function useT() {
  return useI18n().t
}
